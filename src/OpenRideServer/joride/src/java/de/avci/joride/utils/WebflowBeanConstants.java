package de.avci.joride.utils;

/**
 * Constants to be used in webflows. See also class {@link WebflowBean}
 *
 * @author jochen
 */
public class WebflowBeanConstants {

    /**
     * Name of the http request parameter that contains the Address
     */
    public static final String PARAM_NAME_ADDRESS = "address";
    /**
     * Webflow param name for parameter that contains the nextPage of a webflow.
     */
    public static final String PARAM_NAME_NEXT = "next";
    /**
     * Webflow param name for parameter that contains the previousPage of a
     * webflow.
     */
    public static final String PARAM_NAME_BACK = "back";
    /**
     * Webflow param name for parameter that contains the "cancel" Page of a
     * webflow.
     */
    public static final String PARAM_NAME_CANCEL = "cancel";
    /**
     * Webflow param name for parameter that contains the "finish" Page of a
     * webflow.
     */
    public static final String PARAM_NAME_FINISH = "finish";
}
